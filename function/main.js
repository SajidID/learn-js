//learn function

//penjumlahan
function penjumlahan(angka1, angka2) {
  return angka1 + angka2;
}
console.log("30+60=", penjumlahan(30, 60));

//pengurangan
function pengurangan(angka1, angka2) {
  return angka1 - angka2;
}
console.log("2008-11=", pengurangan(2008, 11));

//perkalian
function perkalian(angka1, angka2) {
  return angka1 * angka2;
}
console.log("2008.11=", 2008 * 11);

//pembagian
function pembagian(angka1, angka2) {
  return angka1 - angka2;
}
console.log("2008:11=", 2008 / 11);

